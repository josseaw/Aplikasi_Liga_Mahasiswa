    private void TampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nama Tim");
        model.addColumn("Asal Tim");
        model.addColumn("Nama Pelatih");
        tbldaftar.setModel(model);

//menampilkan data database kedalam tabel
        try {
            PreparedStatement stat = conection.koneksiDB().prepareStatement(""
                    + "SELECT * FROM pendaftaran");
            ResultSet data = stat.executeQuery(); //Menampilkan hasil
            
            while (data.next()) {
                model.addRow(new Object[]{
                    data.getString("nama_tim"),
                    data.getString("asal_tim"),
                    data.getString("nama_pelatih")
                });
                tbldaftar.setModel(model);
            }
        } catch (Exception e) {
            System.err.println("Terjadi Kesalahan :" + e);
        }
    }
